const getChecksum = (string) => {
  let array = string.split('\n');
  const length = array.length;
  let current = {}
  let box = '';
  let threes = 0;
  let twos = 0;
  for (i = 0; i < array.length; i++) {
    let currentThrees = 0;
    let currentTwos = 0;
    box = array[i];
    // console.log(box)
    for (a = 0; a < box.length; a++) {
      if (!current[`${box[a]}`]) {
        current[`${box[a]}`] = 1;
      } else {
        current[`${box[a]}`] = current[`${box[a]}`] + 1;
      }
      switch(current[`${box[a]}`]) {
          case 2:
          currentTwos++;
          break;

          case 3:
          currentTwos--;
          currentThrees++;
          break;
          
          case 4:
          currentThrees--;
          break;

          default:
          break;
        }
    }
    if (currentThrees > 0) {
      threes++;
    }
    if (currentTwos > 0) {
      twos++;
    }
    // console.log(current)
    // console.log(threes, twos)
    current = {};
  }
  // console.log(threes, twos)
  return threes * twos;
}

const findCommon = (string) => {
  let array = string.split('\n');
  const length = array.length;
  const boxLength = array[0].length;
  let wrong = 0;
  let wrongLetter = 0
  let box1 = "";
  let box2 = "";
  for (i = 0; i < length; i++) {
    box1 = array[i];
    for (j = i; j < length; j++) {
      box2 = array[j];
      for (n = 0; n < boxLength; n++) {
        if (box1[n] !== box2[n]) {
          wrongLetter = n;
          wrong++;
        }
        if (wrong > 1) {
          break;
        }
      }
      if (wrong === 1) {
        return [box1, box2, box1.substring(0, wrongLetter) + box1.substring(wrongLetter + 1)]
      } else {
        wrong = 0;
      }
    }
  }
}

